class MapActivity : Fragment() {



    override fun onCreateView(
            inflater: LayoutInflater,
            container: ViewGroup?,
            savedInstanceState: Bundle?
    ): View? {

        val root = inflater.inflate(R.layout.activity_map, container, false)
        val mapa = root.findViewById<ImageView>(R.id.mapa)

        mapa.setOnClickListener{
            // Creates an Intent that will load a map of San Francisco
            val gmmIntentUri = Uri.parse("geo:41.1285201,-8.6101878?q=centro hostipalar de vila nova de gaia")
            val mapIntent = Intent(Intent.ACTION_VIEW, gmmIntentUri)
            mapIntent.setPackage("com.google.android.apps.maps")
            startActivity(mapIntent)
        }


        return root
    }

}
